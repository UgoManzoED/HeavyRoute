package com.heavyroute.resources.enums;

/**
 * Enumerazione che descrive le tipologie di eventi avversi riscontrabili su strada.
 * <p>
 * Utilizzata per categorizzare le segnalazioni inviate dagli autisti o dai sistemi esterni.
 * </p>
 */
public enum RoadEventType {
    ACCIDENT,
    CONSTRUCTION,
    TRAFFIC_JAM,
    WEATHER_CONDITION,
    POLICE_CHECKPOINT,
    OBSTACLE
}